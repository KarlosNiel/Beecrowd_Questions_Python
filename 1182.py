C = int(input())
oper = str(input())
soma = 0

for linha in range(12):
    for coluna in range(12):
        matriz[linha][coluna] = float(input(f"[{linha}][{coluna}]"))

if C == 0:
    for linha in range(12):
        for coluna in range(C, 1):
            soma += matriz[linha][coluna]

if C > 0:
    for linha in range(12):
        for coluna in range(C, C + 1):
            soma += matriz[linha][coluna]

if oper == "S":
    print(f"{soma:.1f}")

elif oper == "M":
    print(f"{soma / 12:.1f}")




